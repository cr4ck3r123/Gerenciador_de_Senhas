    //SETAR CAMPOS
    private void setarCampos() throws ParseException, SQLException {
        int setar = tblUsuario.getSelectedRow();
        ControlUsuario control = new ControlUsuario();
        ModelUsuario modelusuario = new ModelUsuario();
        String tag = tblUsuario.getModel().getValueAt(setar, 4).toString();
        modelusuario = control.controlSelectUser(tag);

        txtNome.setText(tblUsuario.getModel().getValueAt(setar, 1).toString());
        txtGrupo.setSelectedItem(modelusuario.getGrupo());
        txtMaquina.setText(modelusuario.getMaquina());
        txtAnydesk.setText(modelusuario.getAnydesk());
        txtDescricao.setText(modelusuario.getDescricacao());
        txtLocalidade.setText(modelusuario.getLocalidade());
        txtEmail.setText(modelusuario.getEmail());
        txtTag.setText(modelusuario.getTag());
        txtContato.setText(modelusuario.getContato());

        //selecao();
    }

//LISTAR USUARIOS    
    private void carregarUsuarios() throws SQLException {

        listamModelUsuario = controlUsuario.getListaAppControl(cbGrupo.getSelectedItem().toString());
        DefaultTableModel modelo = (DefaultTableModel) tblUsuario.getModel();

        modelo.setNumRows(0);
        //CARREGA OS DADOS DA LISTA NA TABELA
        int cont = listamModelUsuario.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listamModelUsuario.get(i).getId(),
                listamModelUsuario.get(i).getNome(),
                listamModelUsuario.get(i).getGrupo(),
                listamModelUsuario.get(i).getMaquina(),
                listamModelUsuario.get(i).getTag(),
                listamModelUsuario.get(i).getDescricacao(),
                listamModelUsuario.get(i).getLocalidade()
            });

        }

    }

