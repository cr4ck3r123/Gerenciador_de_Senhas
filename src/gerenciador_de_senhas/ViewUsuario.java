    private void tblUsuarioMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuarioMouseReleased
        int setar = tblUsuario.getSelectedRow();
        String grupo = tblUsuario.getModel().getValueAt(setar, 2).toString();
               
        if (!grupo.equals("PC-RESERVA")) {
            if (evt.isPopupTrigger()) {
                int x = evt.getX();
                int y = evt.getY();
                JComponent source = (JComponent) evt.getSource();
                SwingUtilities.convertPointToScreen(new Point(x, y), source);
                jPopupMenu1.show(source, x, y);
                p_troca = 1;
            }
        }
        else if(grupo.equals("PC-RESERVA") && p_troca == 1){
                if (evt.isPopupTrigger()) {
                int x = evt.getX();
                int y = evt.getY();
                JComponent source = (JComponent) evt.getSource();
                SwingUtilities.convertPointToScreen(new Point(x, y), source);
                jPopupMenu2.show(source, x, y);
            }
        }
    }//GEN-LAST:event_tblUsuarioMouseReleased

